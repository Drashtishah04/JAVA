class opr
{
    public static void main(String s[])
    {
        System.out.println("ANS 5|7 Is: "+(5|7));
        System.out.println("ANS 5&7 Is: "+(5&7));
        System.out.println("ANS 5^7 Is: "+(5^7));
        System.out.println("ANS ~7 Is: "+(~7));
        System.out.println("ANS 5>>7 Is: "+(5>>7));
        System.out.println("ANS 5<<7 Is: "+(5<<7));
    }
}